package com.linkysoft.shefaeshop.domain.interactors.impl;

import android.util.Log;

import com.linkysoft.shefaeshop.Network.ApiClient;
import com.linkysoft.shefaeshop.Network.response.CategoryResponse;
import com.linkysoft.shefaeshop.Network.services.HomeCategoryApiInterface;
import com.linkysoft.shefaeshop.domain.executor.Executor;
import com.linkysoft.shefaeshop.domain.executor.MainThread;
import com.linkysoft.shefaeshop.domain.interactors.HomeCategoriesInteractor;
import com.linkysoft.shefaeshop.domain.interactors.base.AbstractInteractor;

import retrofit2.Call;
import retrofit2.Callback;
import retrofit2.Response;

public class HomeCategoriesInteractorImpl extends AbstractInteractor {
    private HomeCategoriesInteractor.CallBack mCallback;
    private HomeCategoryApiInterface apiService;

    public HomeCategoriesInteractorImpl(Executor threadExecutor, MainThread mainThread, HomeCategoriesInteractor.CallBack callBack) {
        super(threadExecutor, mainThread);
        mCallback = callBack;
    }

    @Override
    public void run() {
        apiService = ApiClient.getClient().create(HomeCategoryApiInterface.class);
        Call<CategoryResponse> call = apiService.getHomeCategories();

        call.enqueue(new Callback<CategoryResponse>() {
            @Override
            public void onResponse(Call<CategoryResponse> call, Response<CategoryResponse> response) {
                try {
                    mCallback.onHomeCategoriesDownloaded(response.body().getData());
                } catch (Exception e) {
                    Log.e("Exception", e.getMessage());
                }
            }

            @Override
            public void onFailure(Call<CategoryResponse> call, Throwable t) {
                mCallback.onHomeCategoriesDownloadError();
            }
        });

    }
}
